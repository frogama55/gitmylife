int ascii(char c, int b) {
    int a = (int)c; // charをintに変換するとASCIIコードになる 

    if (a >= 65 && a <= 90) { // 大文字 (A-Z)
        a = a - 65;
    } else if (a >= 97 && a <= 122) { // 小文字 (a-z)
        a = a - 97 + 26; // 大文字の後に続くように調整
    } else if (a >= 48 && a <= 57) { // 数字 (0-9)
        a = a - 48 + 52; // 小文字の後に続くように調整 (26大文字 + 26小文字 = 52)
    } else {
        printf("無効な値です。アルファベットまたは数字を入力してください。\n"); 
        exit(1); // 処理を強制中断
    }

    if (b == 1) { // 筆記体の場合 
        a = a + 62; // 
    }
    return a;
}

